#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long	nb;
	int		digit;

	nb = (long)n;
	if (nb < 0)
	{
		write(fd, "-", 1);
		nb = -nb;
	}
	if (nb > 9)
		ft_putnbr_fd(nb / 10, fd);
	digit = nb % 10 + '0';
	write(fd, &digit, 1);
}
